package src;

public class Operadores04 {
  public static void main(String[] args) { 
     
    boolean c1 = true;
    boolean c2 = false; 
    
    if (c1 && c2) {
        System.out.println("As duas condicões são verdadeiras");      
    }
    if (c1 || c2) {
        System.out.println("UMA das condicões é verdadeira");      
    }
    System.out.println("fim");
  }
}
